<script setup>
import { ref, onMounted } from 'vue';
import axios from 'axios';

const items = ref([]); // Add a ref to store the items
const selectedItems = ref([]); // Add a ref to store the selected items

const fetchItems = async () => {
  try {
    const response = await axios.get('http://localhost:3000/products');
    items.value = response.data;
    console.log(response.data);
  } catch (error) {
    console.error('Error fetching items:', error);
  }
};

const publishToWix = async () => {
  try {
    await axios.post('http://localhost:3000/products/send_to_wix', {
      ids: selectedItems.value,
    });
    alert('Items published to WIX successfully!');
  } catch (error) {
    console.error('Error publishing items to WIX:', error);
  }
};

onMounted(() => {
  fetchItems();
});
</script>

